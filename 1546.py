n = int(input())

for i in range(n):
    m = int(input())
    for j in range(m):
        k = int(input())
        
        if k == 1:
            print('Rolien')
        elif k == 2:
            print('Naej')
        elif k == 3:
            print('Elehcim')
        else:
            print('Odranoel')